#define _CRT_SECURE_NO_WARNINGS
#include <stdio.h>
#include <string.h>
#include <stdlib.h>
int main02()
{
	int a = 3;
	int b = -5;
	int c = a ^ b;
	//按位与
	//b 10000000000000000000000000000101(原码)
	//b 11111111111111111111111111111011（补码）
	//a 00000000000000000000000000000011（补码）
	//& 00000000000000000000000000000011  3

	//按位或
	//b 11111111111111111111111111111011（补码）
	//a 00000000000000000000000000000011（补码）
	//|c11111111111111111111111111111011 (反码)
	//  10000000000000000000000000000101(原码) 
	//c=-5

	//按位异或
	//b 11111111111111111111111111111011（补码）
	//a 00000000000000000000000000000011（补码）
	//c 11111111111111111111111111111000（补码）
	//c 10000000000000000000000000001000（原） -8
	printf("%d\n", c);
	system("pause");
	return 0;
}